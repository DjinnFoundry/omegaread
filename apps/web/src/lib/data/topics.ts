/**
 * Agrupa topics por categoria para el selector UI.
 */
export function getTopicsPorCategoria(edadAnos: number): Map<string, TopicSeed[]> {
  const agrupados = new Map<string, TopicSeed[]>();

  for (const cat of CATEGORIAS) {
    const topicsDeCat = TOPICS_SEED
      .filter(t => t.categoria === cat.slug && edadAnos >= t.edadMinima && edadAnos <= t.edadMaxima)
      .sort((a, b) => a.orden - b.orden);

    if (topicsDeCat.length > 0) {
      agrupados.set(cat.slug, topicsDeCat);
    }
  }

  return agrupados;
}
